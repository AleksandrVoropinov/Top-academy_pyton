# Задание 3
# Пользователь вводит с клавиатуры количество ме-
# тров. В зависимости от выбора пользователя программа
# переводит метры в мили, дюймы или ярды.

metre = float(input('Введите кол-во метров: '))
conversion = input('Выберите во что хотите конвектировать метры: miles, foot, yard\n')

if conversion == 'miles':
    print(metre * 0.000621)
elif conversion == 'foot':
    print(metre * 3.28)
elif conversion == 'yard':
    print(metre * 1.09)
else:
    print("Неверный знак")
