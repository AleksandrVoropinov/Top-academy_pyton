# Задание 2
# Пользователь вводит с клавиатуры три числа. В за-
# висимости от выбора пользователя программа выводит
# на экран максимум из трёх, минимум из трёх или сред-
# неарифметическое трёх чисел.

a = int(input('Введите 1-ое число: '))
b = int(input('Введите 2-ое число: '))
c = int(input('Введите 3-е число: '))

x = [a, b, c]
d = int(input("Выберите действие:\n 1 - найти максимум\n 2 - найти минимум\n 3 - найти среднее \n"))

if d == 1:
    print('Максимум из 3-х чисел: ', max(x))
elif d == 2:
    print('Минимум из 2-х чисел: ', min(x))
elif d == 3:
    print(f'Среднее арифметическое из 3-х чисел: {(a + b + c)/3}')
else:
    print('Такого варианта не существует!')
