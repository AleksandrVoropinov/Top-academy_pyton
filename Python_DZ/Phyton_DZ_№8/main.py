# Задание 1
# Пользователь вводит с клавиатуры три числа. В за-
# висимости от выбора пользователя программа выводит
# на экран сумму трёх чисел или произведение трёх чисел.

a = int(input('Введите 1-ое число: '))
b = int(input('Введите 2-ое число: '))
c = int(input('Введите 3-е число: '))

action = int(input("Выберите действие:\n 1 - сумма чисел\n 2 - произведение чисел\n"))
summ = a + b + c
comp = a * b * c

if action == 1:
    print(f'Сумма трех чисел= {summ}')
elif action == 2:
    print(f'Произведение трех чисел= {comp}')
else:
    print('Такого варианта не существует!')
