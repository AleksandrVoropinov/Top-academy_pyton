# Задание 3
# Напишите программу, вычисляющую площадь ром-
# ба. Пользователь с клавиатуры вводит длину двух его
# диагоналей.

d1 = float(input('Введите длину 1-ой диагонали: '))
d2 = float(input('Введите длину 2-ой диагонали: '))

S = d1 * d2 * 0.5

print(f'Площадь ромба= {S}')
