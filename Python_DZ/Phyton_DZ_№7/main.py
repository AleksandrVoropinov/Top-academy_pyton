# Задание 1
# Пользователь вводит с клавиатуры три числа. Необхо-
# димо найти сумму чисел, произведение чисел. Результат
# вычислений вывести на экран.

a = int(input('Введите 1-ое число: '))
b = int(input('Введите 2-ое число: '))
c = int(input('Введите 3-е число: '))

summ = a + b + c
comp = a * b * c

print(f'Сумма чисел= {summ}\nПроизведение чисел= {comp}')
